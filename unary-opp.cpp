// C++ program to demonstrate the use of 'unary minus'
// operator

#include <iostream>
using namespace std;

int main()
{
	int positiveInteger = 100;
	int negativeInteger = -positiveInteger;

	cout << "Positive Integer: " << positiveInteger << endl;
	cout << "Negative Integer: " << negativeInteger << endl;

	return 0;
}

